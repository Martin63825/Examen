package model;

public class PlanetasModelo {
	private double UA;
	private Long id;
	private Integer milesk;
	private String nombre;
	public double getUA() {
		return UA;
	}
	public void setUA(double uA) {
		UA = uA;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Integer getMilesk() {
		return milesk;
	}
	public void setMilesk(Integer milesk) {
		this.milesk = milesk;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
}
	